package stringConcept;

//Returns the character at ith index.
public class CharAt {

	public static void main(String[] args) {
		// Counting will be started from 0 :
		String s1 = "selenium";
		String s2 = "geekforgeeks";
		String s3 = "My name is Maneesh mishra";
		System.out.println("S1: " + s1.charAt(3));
		System.out.println("s2: " + s1.charAt(6));
		System.out.println("s2: " + s2.charAt(7));
		System.out.println("s2: " + s2.charAt(10));
		System.out.println("s3: " + s3.charAt(15));
		System.out.println("s3: " + s3.charAt(10));

	}

}
