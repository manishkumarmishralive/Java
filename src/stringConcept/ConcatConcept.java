package stringConcept;

//Concatenates specified string to the end of this string.
public class ConcatConcept {

	public static void main(String[] args) {
		String s1 = "Selenium";
		String s2 = "Java";
		String s3 = "hello";
		System.out.println("String : " + s1.concat(s2));
		System.out.println("String  :" + s3.concat(s2));

	}

}
